package com.example.eureka.shared.config.openapi;

import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.http.*;
import org.springframework.stereotype.Service;
import org.springframework.web.client.RestTemplate;

import java.util.List;
import java.util.Map;

@Slf4j
@Service
@RequiredArgsConstructor
public class GPTRoadmap {

    private final RestTemplate restTemplate = new RestTemplate();
    @Value("${open-ai-api-key:}")
    private String apiKey;

    @Value("${open-ai-api-url:}")
    private String apiUrl;
    public String generarRoadmap(String historia, String objetivo) {
        log.info("API KEY length = {}", apiKey != null ? apiKey.length() : 0);
        log.info("API URL = {}", apiUrl);
        String prompt = """
    Genera un roadmap de 5 fases para un emprendimiento.
    Fases: Idea, Validaci贸n, Planificaci贸n, Desarrollo, Lanzamiento.
    
    Por cada fase genera de 1 a 6 ideas breves y accionables.
    
    Historia: %s
    Objetivo: %s

    Responde exclusivamente en JSON con el formato EXACTO:
    [
        {"fase": "Idea", "ideas": ["Idea 1", "Idea 2", "..."]},
        {"fase": "Validaci贸n", "ideas": ["Idea 1", "Idea 2", "..."]},
        {"fase": "Planificaci贸n", "ideas": ["Idea 1", "Idea 2", "..."]},
        {"fase": "Desarrollo", "ideas": ["Idea 1", "Idea 2", "..."]},
        {"fase": "Lanzamiento", "ideas": ["Idea 1", "Idea 2", "..."]}
    ]

    No agregues texto fuera del JSON.
    """.formatted(historia, objetivo);


        Map<String, Object> request = Map.of(
                "model", "gpt-4o-mini",
                "messages", List.of(
                        Map.of("role", "user", "content", prompt)
                )
        );

        HttpHeaders headers = new HttpHeaders();
        headers.setContentType(MediaType.APPLICATION_JSON);
        headers.set("Authorization", "Bearer " + apiKey);

        HttpEntity<Map<String, Object>> entity = new HttpEntity<>(request, headers);

        ResponseEntity<Map> response = restTemplate.exchange(
                apiUrl,
                HttpMethod.POST,
                entity,
                Map.class
        );

        Map<String, Object> body = response.getBody();
        List<Map<String, Object>> choices = (List<Map<String, Object>>) body.get("choices");
        Map<String, Object> firstChoice = choices.get(0);
        Map<String, Object> message = (Map<String, Object>) firstChoice.get("message");
        String content = (String) message.get("content");

        return content;

    }
}
